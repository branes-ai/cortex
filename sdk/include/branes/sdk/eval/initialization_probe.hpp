// branes/sdk/eval/initialization_probe.hpp — the S1 (initialization) probe of
// the VIO contract program (docs/arch/vio-pipeline-canonical.md).
//
// S1 bootstraps the manifold: orientation (gravity-aligned), gyro bias, and —
// on the dynamic path — gravity, per-keyframe velocity, and the metric SCALE.
// Its contract:
//   • static: the recovered R levels the measured gravity (post-alignment
//     residual ≈ 0), |g| is sane, the stationarity gate rejects motion;
//   • dynamic: scale is recovered when motion excites it, and the
//     observability gate DECLINES (rather than returns garbage) when it doesn't;
//   • the initial covariance honestly reflects what was not observed.
//
// Native units: m/s² (gravity residual / magnitude), degrees (attitude /
// gravity-direction error), % (scale error), m (resolved motion), and the
// initial-P σ per block (deg, m, m/s²). The probe drives the REAL ImuInitializer
// and the REAL ImuPreintegrator, so it tests the shipped code; the dynamic
// keyframes are generated by preintegrating a synthetic trajectory, exactly as
// the live pipeline does.
//
// Header-only, C++20, type-generic.

