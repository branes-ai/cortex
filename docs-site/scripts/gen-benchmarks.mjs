// Extract the benchmark *gates* from their single source of truth — the
// C++ test thresholds and the CI config — into src/data/benchmarks.json,
// which the Benchmarks pages render. This makes the gate numbers on the
// site impossible to drift from the code: they are regenerated on every
// build (prebuild/predev hooks) and this script HARD-FAILS if any value
// can no longer be located (e.g. a constant was renamed) or is out of a
// sane range, so a format/unit change is caught at build time rather than
// silently shipping stale or wrong docs.
//
// Note: only *gates/budgets* are pinned here. Observed measurements (e.g.
// "~38 ms median at -O2") are reference numbers, not gates, and stay as
// clearly-labeled prose in the pages.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url)); // docs-site/scripts
const repo = resolve(here, '..', '..'); // repository root

function read(rel) {
  return readFileSync(resolve(repo, rel), 'utf8');
}

// Strip C/C++ comments so a regex can't match a commented-out value (the
// ':' guard avoids eating the // in URLs / strings like http://).
function stripCppComments(s) {
  return s.replace(/\/\*[\s\S]*?\*\//g, '').replace(/(^|[^:])\/\/.*$/gm, '$1');
}

function extract(text, re, what) {
  const m = text.match(re);
  if (!m) {
    throw new Error(
      `gen-benchmarks: could not extract ${what}. The source format likely changed; ` +
        `update docs-site/scripts/gen-benchmarks.mjs so the docs stay pinned to the code.`,
    );
  }
  return m[1];
}

// Validate v ∈ (lo, hi] by default, or [lo, hi] when inclusiveLo is set.
// Latency/ATE gates must be strictly positive (a zero gate is meaningless);
// the coverage floor may legitimately be 0 (no gate), so it uses [0, 100].
function inRange(v, lo, hi, what, inclusiveLo = false) {
  const okLo = inclusiveLo ? v >= lo : v > lo;
  if (!Number.isFinite(v) || !okLo || !(v <= hi)) {
    const lb = inclusiveLo ? '[' : '(';
    throw new Error(`gen-benchmarks: ${what} = ${v} is outside the sane range ${lb}${lo}, ${hi}] — likely a typo or unit change.`);
  }
  return v;
}

// Latency budget: scope the match to each specialization's braces so it is
// independent of the order the specializations appear in (and of any third
// specialization a maintainer might add).
const latencyHpp = stripCppComments(read('sdk/include/branes/sdk/eval/latency_budget.hpp'));
function latencyBudget(spec) {
  const re = new RegExp(`FrameLatencyBudget<${spec}>\\s*\\{[^}]*?per_frame_ms\\s*=\\s*([0-9.]+)`);
  return Number(extract(latencyHpp, re, `${spec} latency budget (FrameLatencyBudget<${spec}>::per_frame_ms)`));
}
const latencyDouble = inRange(latencyBudget('double'), 0, 10000, 'double latency budget (ms)');
const latencyFloat = inRange(latencyBudget('float'), 0, 10000, 'float latency budget (ms)');

// EuRoC ATE gate: anchor on the REQUIRE assertion, not the INFO log line.
const eurocTest = stripCppComments(read('tests/sdk_vio_euroc_test.cpp'));
const ateGate = inRange(
  Number(extract(eurocTest, /REQUIRE\(\s*ate\s*<\s*([0-9.]+)\s*\)/, 'EuRoC V1_01_easy ATE gate (REQUIRE(ate < …))')),
  0,
  100,
  'EuRoC ATE gate (m)',
);

// Coverage floor: anchor on the actual cargo command, not the prose comment
// above it that also mentions the flag.
const ciYaml = read('.github/workflows/ci.yml');
const coverageFloor = inRange(
  Number(extract(ciYaml, /cargo llvm-cov[^\n]*--fail-under-lines\s+(\d+)/, 'Rust coverage line floor (cargo llvm-cov --fail-under-lines)')),
  0,
  100,
  'coverage floor (%)',
  true, // inclusiveLo: a 0% floor (no gate) is valid, per the 0–100 contract
);

const data = {
  _comment: 'GENERATED by docs-site/scripts/gen-benchmarks.mjs — do not edit by hand.',
  _source: 'Extracted from the C++ test gates and CI config (the single source of truth).',
  latency_budget_ms: { double: latencyDouble, float: latencyFloat },
  euroc_v101_ate_gate_m: ateGate,
  coverage_floor_pct: coverageFloor,
};

const outDir = resolve(here, '..', 'src', 'data');
mkdirSync(outDir, { recursive: true });
writeFileSync(resolve(outDir, 'benchmarks.json'), JSON.stringify(data, null, 2) + '\n');
console.log('gen-benchmarks: wrote src/data/benchmarks.json', data);
